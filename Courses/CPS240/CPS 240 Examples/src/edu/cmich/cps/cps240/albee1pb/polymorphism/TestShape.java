package edu.cmich.cps.cps240.albee1pb.polymorphism;

/**
 * Test driver for shape hierarchy.
 * 
 * @author albee
 *
 * @version $Id$
 */
public class TestShape {

	/**
	 * Exercise the shape hierarchy.
	 * 
	 * @param args Command line arguments, ignored.
	 */
	public static void main(String[] args) {
		Circle circle = new Circle();
		Rectangle rectangle = new Rectangle();
		Star star = new Star();
		
		
		drawMe(circle);
		drawMe(rectangle);
		drawMe(star);
	}
	
	static void drawMe(Shape s){
		s.draw();
	}

}
